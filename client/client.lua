function IsWeaponHave(WEAPON_NAME)
    local PLAYER_DATA = QBCore.Functions.GetPlayerData()
    local INVENTORY_ITEMS = PLAYER_DATA.items 
    local PLAYER_WEAPON = GetSelectedPedWeapon(PlayerPedId())
    local WEAPON_NAME = WEAPON_LIST[tostring((PLAYER_WEAPON))]

    for k, v in pairs(INVENTORY_ITEMS) do 
        if v.name == WEAPON_NAME then 
            return true 
        end 
    end
    return false 
end

Citizen.CreateThread(function()
    while true do 
        local PLAYER_WEAPON = GetSelectedPedWeapon(PlayerPedId())

        if PLAYER_WEAPON ~= nil and PLAYER_WEAPON ~= GetHashKey("WEAPON_UNARMED") then 
            local WEAPON_NAME = WEAPON_LIST[tostring((PLAYER_WEAPON))]
            -- if exports["nightv-warzone"]:inZone() then return end 

            if not IsWeaponHave(WEAPON_NAME) then 
                -- any ban or kick event 
                RemoveWeaponFromPed(PlayerPedId(), PLAYER_WEAPON)
            end
        end
        Wait(1000)
    end
end)
